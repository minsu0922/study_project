## 왜 스레드를 쓰나
프로세스를 새로 만드는 것(fork)은 메모리 공간을 통째로 새로 준비해야 해서 비용이 크다. 스레드는 이미 있는 프로세스의 메모리를 그대로 쓰면서 실행 흐름만 하나 더 늘리는 것이라 생성과 전환(컨텍스트 스위칭) 비용이 훨씬 싸다. 대신 메모리를 공유하기 때문에 여러 스레드가 같은 데이터를 동시에 건드리면 경쟁 상태(race condition)가 생길 수 있어, 락 같은 동기화 장치가 필요해진다.

