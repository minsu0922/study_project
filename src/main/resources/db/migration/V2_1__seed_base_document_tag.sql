## 프로세스란
실행 중인 프로그램 하나를 프로세스라 한다. 각 프로세스는 자신만의 독립된 메모리 공간(코드, 데이터, 힙, 스택)을 운영체제로부터 할당받는다. 프로세스 A가 아무리 이상하게 동작해도 프로세스 B의 메모리를 직접 건드릴 수 없다. 이것이 프로세스 간 격리다.

## 스레드란
하나의 프로세스 안에서 실제로 실행되는 흐름의 단위다. 프로세스가 회사라면 스레드는 그 회사에서 일하는 직원이라고 볼 수 있다. 한 프로세스는 스레드를 여러 개 가질 수 있고(멀티스레드), 이 스레드들은 같은 프로세스의 코드, 데이터, 힙 영역을 공유한다. 다만 스택만은 스레드마다 따로 가진다. 각자 독립적인 함수 호출 흐름과 지역변수를 가져야 하기 때문이다.

