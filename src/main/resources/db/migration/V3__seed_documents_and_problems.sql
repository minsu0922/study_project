## 문제: HTTP는 기억력이 없다
HTTP는 **무상태(stateless)** — 요청 하나하나가 남남이다.
그래서 "아까 로그인한 그 사람"임을 매 요청마다 증명할 방법이 필요하다. 방식이 두 갈래다.

## 방식 1: 세션 (서버가 기억)
1. 로그인 성공 → 서버가 세션 저장소에 "세션ID=철수" 기록
2. 세션ID를 쿠키로 브라우저에 준다
3. 매 요청마다 쿠키의 세션ID로 저장소를 조회해 신원 확인

- 장점: 서버가 세션을 지우면 **즉시 로그아웃** 가능 (통제력)
- 단점: 서버가 상태를 들고 있다 → 서버를 여러 대로 늘리면 세션 공유 문제 (sticky session, Redis 공유 등)

## 방식 2: JWT (토큰 자체가 증명서)
1. 로그인 성공 → 서버가 사용자 정보(id, 역할)에 **서명**한 토큰(JWT)을 발급
2. 브라우저가 저장했다가 매 요청 Authorization 헤더로 보냄
3. 서버는 **서명만 검증**하면 끝 — 저장소 조회가 필요 없다

```
JWT = header.payload.signature
      (알고리즘) . (사용자 정보, 만료시간) . (비밀키로 서명)
```

- 장점: 서버 무상태 유지 → 수평 확장이 쉽다. 이 프로젝트가 JWT를 쓰는 이유.
- 단점: **발급한 토큰을 회수할 수 없다** — 만료 전까지는 유효하다. 탈취되면 만료까지 위험.
- 주의: payload는 암호화가 아니라 **인코딩**(base64)이다. 누구나 읽을 수 있으니 비밀은 넣지 않는다. 서명은 "위조 방지"용이지 "숨기기"용이 아니다.

