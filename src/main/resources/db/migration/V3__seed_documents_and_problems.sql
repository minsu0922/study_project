## 페이지 폴트 (page fault)
접근한 페이지가 물리 메모리에 없으면 발생하는 **예외**다. OS가 처리한다:
1. 디스크(스왑 영역)에서 해당 페이지를 찾는다
2. 빈 프레임에 올린다 (없으면 교체 알고리즘으로 희생 페이지 선정 — LRU 등)
3. 페이지 테이블 갱신 후 명령을 재실행

폴트가 너무 잦아 실행보다 스왑에 시간을 다 쓰는 상태를 **스래싱(thrashing)** 이라 한다.

