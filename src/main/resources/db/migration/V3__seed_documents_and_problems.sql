'# TCP 3-way 핸드셰이크와 연결 종료

## 왜 필요한가
TCP는 **연결지향** 프로토콜이다. 데이터를 보내기 전에 "서로 통신할 준비가 됐는지"를 확인하는 절차가 필요한데, 그것이 3-way 핸드셰이크다.
전화를 걸 때 "여보세요?" → "네, 말씀하세요" → "네, 그럼 시작할게요"를 주고받는 것과 같다.

## 연결 수립 (3-way handshake)
```
클라이언트                     서버
    | ------- SYN(seq=x) ------> |   1. 접속 요청 + 내 시작 번호 x
    | <-- SYN+ACK(seq=y,ack=x+1) |   2. 수락 + 서버 시작 번호 y + x 잘 받음
    | ------- ACK(ack=y+1) ----> |   3. y도 잘 받음 → 연결 성립(ESTABLISHED)
```

- **왜 2번이 아니라 3번인가?** 2번만 주고받으면 서버는 "클라이언트가 내 응답(SYN+ACK)을 받았는지" 알 수 없다. 옛날에 지연됐던 낡은 SYN이 뒤늦게 도착해 유령 연결이 생기는 것도 3번째 ACK가 막아 준다.
- **seq(시퀀스 번호)를 난수에서 시작하는 이유**: 이전 연결의 세그먼트와 새 연결의 세그먼트가 섞이는 것을 방지하고, 번호 추측 공격을 어렵게 한다.

## 연결 종료 (4-way handshake)
```
클라이언트                     서버
    | ------- FIN ------->      |   1. 나는 다 보냈어
    | <------ ACK               |   2. 알았어 (아직 서버는 보낼 게 남았을 수 있음)
    | <------ FIN               |   3. 나도 다 보냈어
    | ------- ACK ------->      |   4. 알았어 → 종료
```

- 수립은 3번인데 종료가 4번인 이유: **양쪽의 "보내기 끝"이 따로**이기 때문. 서버는 ACK로 일단 받았다고 답한 뒤, 남은 데이터를 마저 보내고 나서야 자기 FIN을 보낸다(half-close).
- **TIME_WAIT**: 마지막 ACK를 보낸 쪽은 바로 소켓을 닫지 않고 잠시(2 MSL) 기다린다. 마지막 ACK가 유실되면 서버가 FIN을 재전송하는데, 그때 응답해 줄 주체가 남아 있어야 하기 때문.

