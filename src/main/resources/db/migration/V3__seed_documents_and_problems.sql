## 인덱스 = 책 뒤의 색인
인덱스가 없으면 DB는 원하는 행을 찾기 위해 테이블을 처음부터 끝까지 읽는다(**풀 스캔**, O(N)).
인덱스는 "값 → 행 위치"를 미리 정렬해 둔 자료구조라서 O(log N)으로 찾는다.

## 후보 자료구조 비교
| 구조 | 탐색 | 범위 검색 | 비고 |
|---|---|---|---|
| 해시 테이블 | O(1) | ❌ 불가 | = 검색만 가능, BETWEEN/ORDER BY 무력 |
| 이진 탐색 트리 | O(log N) | 가능 | 한 노드에 키 1개 → 트리가 높아짐 = 디스크 IO 증가 |
| **B+트리** | O(log N) | ✅ 강함 | 한 노드에 키 수백 개 → 트리가 낮음, 리프가 연결 리스트 |

## B+트리가 이긴 이유 — 디스크 IO
DB의 병목은 CPU가 아니라 **디스크 읽기 횟수**다. 디스크는 페이지(보통 16KB) 단위로 읽으므로,
한 번 읽을 때 키를 최대한 많이 담는 넓고 낮은 트리가 유리하다.
수천만 행도 B+트리 높이는 보통 3~4 — 즉 디스크 3~4번 읽기로 어떤 행이든 찾는다.

또한 B+트리는 **리프 노드들이 정렬된 연결 리스트**로 이어져 있어
`BETWEEN`, `ORDER BY`, `LIKE ''ab%''` 같은 범위 검색이 리프를 따라 쭉 읽기만 하면 된다.

