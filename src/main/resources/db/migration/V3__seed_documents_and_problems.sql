'# 시간복잡도와 빅오 표기법

## 왜 필요한가
"이 코드 빠른가요?"에 초 단위로 답하면 컴퓨터 사양마다 달라진다.
**입력 크기 N이 커질 때 연산 횟수가 어떤 비율로 늘어나는가**로 답하는 것이 빅오(Big-O)다.

## 대표 복잡도 (좋은 순)
| 빅오 | 이름 | 예 | N=100만일 때 감각 |
|---|---|---|---|
| O(1) | 상수 | 배열 인덱스 접근, 해시 조회(평균) | 1번 |
| O(log N) | 로그 | 이진 탐색, B+트리 탐색 | 약 20번 |
| O(N) | 선형 | 배열 순회, 풀 스캔 | 100만 번 |
| O(N log N) | 선형로그 | 좋은 정렬(병합·힙), 퀵 정렬 평균 | 약 2천만 번 |
| O(N²) | 제곱 | 이중 반복문, 버블 정렬 | 1조 번 💀 |

## 규칙 두 가지
1. **상수는 버린다**: O(2N) → O(N). N이 커지면 상수 차이는 의미가 없다.
2. **가장 큰 항만 남긴다**: O(N² + N) → O(N²).

## 자주 헷갈리는 것
- **퀵 정렬**: 평균 O(N log N), **최악 O(N²)** (피벗이 계속 치우칠 때). 그래도 실무에서 널리 쓰이는 이유는 평균이 빠르고 캐시 친화적이라서.
- **해시 테이블**: 평균 O(1), 최악 O(N) (충돌이 한 버킷에 몰릴 때).
- **공간 복잡도**: 시간뿐 아니라 추가 메모리도 같은 방식으로 표기한다. 병합 정렬은 시간 O(N log N)이지만 공간 O(N)이 필요하다.

