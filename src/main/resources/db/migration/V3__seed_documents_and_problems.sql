## 트랜잭션 = 전부 아니면 전무
계좌 이체는 "A에서 빼기"와 "B에 넣기"가 **둘 다** 성공하거나 **둘 다** 실패해야 한다.
이런 작업 묶음이 트랜잭션이고, 지켜야 할 성질이 **ACID**다.

- **A**tomicity(원자성): 전부 성공 or 전부 취소(rollback)
- **C**onsistency(일관성): 트랜잭션 전후로 데이터 규칙이 깨지지 않음
- **I**solation(격리성): 동시에 실행돼도 서로 간섭하지 않는 것처럼 보임
- **D**urability(지속성): 커밋되면 장애가 나도 남는다

